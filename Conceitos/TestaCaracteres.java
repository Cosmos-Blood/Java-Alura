package Conceitos;

public class TestaCaracteres {
	
	public static void main(String[] args) {
		/* char: usado para mostrar apenas uma letra dentre de aspas simples.
		 Números dentro do char se tornaram letras!*/
		char letra = 'a';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
	
		//String: Usado para citar uma frase.
		String palavra = "Outro haha mundo";
		System.out.println(palavra);
		
		palavra = palavra + 2020;
		System.out.println(palavra);
		
	
		
		
		
		
	}

}
