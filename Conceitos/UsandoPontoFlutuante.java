package Conceitos;

public class UsandoPontoFlutuante {
	public static void main (String [] args) {
		
		double salario = 1250.00;
		
		System.out.println("O salário mínimo dos funcionários é de: " + salario);
		
		// Cálculo feito dentro dos números reais
		double divisao = 5.0 / 2;
		System.out.println(divisao);
		
		// Cálculo feito dentro dos números inteiros
		int outra_divisao = 5 / 2;
		System.out.println(outra_divisao);
		
		
	}
	

}
